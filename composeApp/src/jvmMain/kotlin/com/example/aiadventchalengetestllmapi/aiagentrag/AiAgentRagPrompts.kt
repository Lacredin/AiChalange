package com.example.aiadventchalengetestllmapi.aiagentrag

object AiAgentRagPrompts {
    const val NO_ANSWER = """
Ответ: Недостаточно данных в переданном контексте для точного ответа.

Источники:
- нет релевантных источников
"""

    const val SYSTEM =
        "Ты RAG-ассистент. Отвечай только на основании переданных: " +
            "retrieval-контекста, краткой истории диалога и task state. " +
            "Не выдумывай факты и не опирайся на внешние знания. " +
            "Если данных недостаточно, прямо скажи об этом.\n\n" +
            "Формат обязателен в каждом ответе:\n" +
            "Ответ: ...\n" +
            "Источники:\n" +
            "- [S1] title | section | path\n" +
            "  Цитата: \"дословная цитата\"\n\n" +
            "Показывай только те источники, которые непосредственно использовал в ответе. " +
            "Если релевантных источников нет, выведи: \"нет релевантных источников\"."

    const val EMPTY_CONTEXT = "Релевантный retrieval-контекст не найден."

    fun buildUserPrompt(
        question: String,
        retrievalQuery: String,
        context: String,
        dialogHistory: String,
        taskState: String
    ): String =
        "Текущий вопрос:\n$question\n\n" +
            "Поисковый запрос (после rewrite, если включен):\n$retrievalQuery\n\n" +
            "Краткая история диалога:\n$dialogHistory\n\n" +
            "Task state (цель, уточнения, ограничения, термины):\n$taskState\n\n" +
            "Retrieval-контекст:\n$context\n\n" +
            "Требования:\n" +
            "1) Ответ должен быть основан только на блоках выше.\n" +
            "2) В разделе \"Источники\" выводи только реально использованные источники.\n" +
            "3) Под каждым источником обязательно выводи строку \"Цитата: ...\".\n" +
            "4) Если данных недостаточно, напиши это в разделе \"Ответ\" и укажи \"нет релевантных источников\"."
}
